def _apply_filters(fcs: Dict[str, Optional[Dict[str, Any]]], section_keys: Set[str]) -> Dict[str, Optional[Dict[str, Any]]]:
    if not section_keys:
        return fcs
    filtered: Dict[str, Optional[Dict[str, Any]]] = {}
    filtered_sections = None
    if isinstance(fcs.get("sections"), Mapping):
        filtered_sections = _filter_feature_collection(fcs["sections"], section_keys)
    section_mask = _build_section_filter_mask(filtered_sections)
    for name, fc in fcs.items():
        if fc is None:
            filtered[name] = None
            continue
        if name == "sections":
            filtered[name] = filtered_sections if filtered_sections is not None else _filter_feature_collection(fc, section_keys)
            continue
        if name == "cad_lines":
            filtered[name] = _filter_feature_collection_by_mask(fc, section_mask)
            continue
        filtered[name] = _filter_feature_collection(fc, section_keys)
    return filtered


