def _read_json(path: Path) -> Optional[Dict[str, Any]]:
    if not path.exists():
        return None
    with path.open("r", encoding="utf-8-sig") as f:
        return json.load(f)


def _feature_collection(data: Optional[Mapping[str, Any]]) -> Optional[Dict[str, Any]]:
    if not data:
        return None
    if isinstance(data, dict) and data.get("type") == "FeatureCollection":
        return dict(data)
    return None


def _iter_coords(coord_obj: Any) -> Iterable[Tuple[float, float]]:
    if isinstance(coord_obj, (list, tuple)) and len(coord_obj) == 2:
        x, y = coord_obj
        if isinstance(x, (int, float)) and isinstance(y, (int, float)):
            yield float(x), float(y)
            return
    if isinstance(coord_obj, (list, tuple)):
        for item in coord_obj:
            yield from _iter_coords(item)


def _parse_zone(value: Any) -> Optional[int]:
    if value is None:
        return None
    try:
        zone = int(str(value).strip())
    except (TypeError, ValueError):
        return None
    if 1 <= zone <= 60:
        return zone
    return None


def _transform_coords_recursive(coord_obj: Any, transformer) -> Any:
    if isinstance(coord_obj, (list, tuple)) and len(coord_obj) == 2:
        x, y = coord_obj
        if isinstance(x, (int, float)) and isinstance(y, (int, float)):
            lon, lat = transformer.transform(float(x), float(y))
            return [lon, lat]
    if isinstance(coord_obj, (list, tuple)):
        return [_transform_coords_recursive(item, transformer) for item in coord_obj]
    return coord_obj


def _normalize_cad_lines_geojson(
    fc: Optional[Dict[str, Any]],
    zone_override: Optional[int] = None,
) -> Tuple[Optional[Dict[str, Any]], Dict[str, int]]:
    stats = {"total": 0, "transformed": 0, "already_lonlat": 0, "untransformed": 0}
    if fc is None:
        return None, stats

    features = fc.get("features")
    if not isinstance(features, list):
        return fc, stats

    try:
        from pyproj import Transformer
    except Exception:
        return fc, stats

    transformers: Dict[int, Any] = {}
    out_features = []

    for feature in features:
        if not isinstance(feature, Mapping):
            continue
        stats["total"] += 1
        geom = feature.get("geometry")
        if not isinstance(geom, Mapping):
            out_features.append(feature)
            continue

        coords = geom.get("coordinates")
        first = next(_iter_coords(coords), None)
        if first is None:
            out_features.append(feature)
            continue

        x, y = first
        if -180.0 <= x <= 180.0 and -90.0 <= y <= 90.0:
            stats["already_lonlat"] += 1
            out_features.append(feature)
            continue

        props = feature.get("properties")
        zone = zone_override
        if zone is None and isinstance(props, Mapping):
            zone = _parse_zone(props.get("zone"))
        if zone is None and isinstance(fc.get("properties"), Mapping):
            zone = _parse_zone(fc.get("properties", {}).get("zone"))

        if zone is None:
            stats["untransformed"] += 1
            out_features.append(feature)
            continue

        transformer = transformers.get(zone)
        if transformer is None:
            transformer = Transformer.from_crs(f"EPSG:326{zone}", "EPSG:4326", always_xy=True)
            transformers[zone] = transformer

        new_feature = dict(feature)
        new_geom = dict(geom)
        new_geom["coordinates"] = _transform_coords_recursive(coords, transformer)
        new_feature["geometry"] = new_geom
        out_features.append(new_feature)
        stats["transformed"] += 1

    return {"type": "FeatureCollection", "features": out_features}, stats


def _annotate_cad_line_ids(fc: Optional[Dict[str, Any]]) -> Optional[Dict[str, Any]]:
    if fc is None or not isinstance(fc, Mapping):
        return fc
    features = fc.get("features")
    if not isinstance(features, list):
        return fc

    seen_counts: Dict[str, int] = {}
    out_features: list[Dict[str, Any]] = []
    for idx, feature in enumerate(features, start=1):
        if not isinstance(feature, Mapping):
            continue
        props = feature.get("properties")
        props_dict = dict(props) if isinstance(props, Mapping) else {}
        layer = str(props_dict.get("layer", "")).strip().upper() or "UNKNOWN"
        handle = str(props_dict.get("handle", "")).strip().upper()
        base_id = f"CAD:{layer}:{handle}" if handle else f"CAD:{layer}:IDX{idx:06d}"
        seen_counts[base_id] = seen_counts.get(base_id, 0) + 1
        suffix = seen_counts[base_id]
        line_id = base_id if suffix == 1 else f"{base_id}#{suffix}"
        props_dict["line_id"] = line_id
        props_dict.setdefault("name", line_id)
        props_dict.setdefault("label", f"Layer: {layer}")

        new_feature = dict(feature)
        new_feature["properties"] = props_dict
        out_features.append(new_feature)

    return {"type": "FeatureCollection", "features": out_features}


def _build_cad_line_id_points(cad_fc: Optional[Mapping[str, Any]]) -> list[Dict[str, Any]]:
    points: list[Dict[str, Any]] = []
    if not isinstance(cad_fc, Mapping):
        return points
    features = cad_fc.get("features")
    if not isinstance(features, list):
        return points

    for feature in features:
        if not isinstance(feature, Mapping):
            continue
        geom = feature.get("geometry")
        props = feature.get("properties")
        if not isinstance(geom, Mapping) or not isinstance(props, Mapping):
            continue
        if geom.get("type") != "LineString":
            continue
        coords = geom.get("coordinates")
        if not isinstance(coords, list) or len(coords) < 2:
            continue
        line_id = str(props.get("line_id", "")).strip()
        if not line_id:
            continue
        try:
            p0 = coords[0]
            p1 = coords[-1]
            x0, y0 = float(p0[0]), float(p0[1])
            x1, y1 = float(p1[0]), float(p1[1])
        except Exception:
            continue
        points.append(
            {
                "position": [0.5 * (x0 + x1), 0.5 * (y0 + y1)],
                "line_id": line_id,
                "layer": str(props.get("layer", "")).strip().upper(),
                "handle": str(props.get("handle", "")).strip().upper(),
            }
        )
    return points


def _find_cad_line_id_matches(
    cad_fc: Optional[Mapping[str, Any]],
    token: str,
    limit: int = 12,
) -> list[str]:
    if not isinstance(cad_fc, Mapping):
        return []
    needle = re.sub(r"\s+", "", str(token or "").strip().upper())
    if not needle:
        return []

    features = cad_fc.get("features")
    if not isinstance(features, list):
        return []

    candidates: list[tuple[int, str]] = []
    seen: set[str] = set()
    for feature in features:
        if not isinstance(feature, Mapping):
            continue
        props = feature.get("properties")
        if not isinstance(props, Mapping):
            continue
        line_id = str(props.get("line_id", "")).strip()
        if not line_id:
            continue
        layer = str(props.get("layer", "")).strip().upper()
        handle = str(props.get("handle", "")).strip().upper()
        line_id_norm = re.sub(r"\s+", "", line_id.upper())
        canonical = f"CAD:{layer}:{handle}" if layer and handle else ""
        canonical_norm = re.sub(r"\s+", "", canonical.upper())
        handle_norm = re.sub(r"\s+", "", handle.upper())

        score = -1
        if needle == line_id_norm:
            score = 100
        elif line_id_norm.endswith(":" + needle):
            score = 95
        elif needle == canonical_norm:
            score = 93
        elif needle == handle_norm:
            score = 90
        elif needle in line_id_norm:
            score = 80
        elif needle in canonical_norm:
            score = 75
        elif needle in handle_norm:
            score = 70

        if score < 0:
            continue
        if line_id in seen:
            continue
        seen.add(line_id)
        candidates.append((score, line_id))

    candidates.sort(key=lambda row: (-row[0], row[1]))
    return [line_id for _, line_id in candidates[: max(1, int(limit))]]


def _bounds_from_feature_collection(fc: Optional[Mapping[str, Any]]) -> Optional[Tuple[float, float, float, float]]:
    if not fc or not isinstance(fc, Mapping):
        return None
    features = fc.get("features", [])
    if not isinstance(features, list):
        return None

    min_x = min_y = float("inf")
    max_x = max_y = float("-inf")
    found = False

    for feature in features:
        if not isinstance(feature, Mapping):
            continue
        geom = feature.get("geometry")
        if not isinstance(geom, Mapping):
            continue
        coords = geom.get("coordinates")
        for x, y in _iter_coords(coords):
            min_x = min(min_x, x)
            max_x = max(max_x, x)
            min_y = min(min_y, y)
            max_y = max(max_y, y)
            found = True

    if not found:
        return None
    return min_x, min_y, max_x, max_y


def _bounds_center_zoom(bounds: Tuple[float, float, float, float]) -> Tuple[float, float, float]:
    min_x, min_y, max_x, max_y = bounds
    cx = 0.5 * (min_x + max_x)
    cy = 0.5 * (min_y + max_y)
    dx = max_x - min_x
    dy = max_y - min_y
    span = max(dx, dy, 1e-9)
    # 360 deg / span_deg roughly doubles each level of zoom.
    zoom = max(4.0, min(18.0, 18 - (math.log2(span * 360.0) if span > 0 else 0.0)))
    return cx, cy, zoom


def _append_linestring_features(
    geom_obj: Any,
    out_features: list[Dict[str, Any]],
    properties: Mapping[str, Any],
) -> None:
    try:
        geom_type = geom_obj.geom_type
    except Exception:
        return

    if geom_type == "LineString":
        coords = list(getattr(geom_obj, "coords", []))
        if len(coords) >= 2:
            out_features.append(
                {
                    "type": "Feature",
                    "properties": dict(properties),
                    "geometry": {"type": "LineString", "coordinates": [[x, y] for x, y in coords]},
                }
            )
        return

    if geom_type in {"MultiLineString", "GeometryCollection"}:
        for part in getattr(geom_obj, "geoms", []):
            _append_linestring_features(part, out_features, properties)


def _append_polygon_features(
    geom_obj: Any,
    out_features: list[Dict[str, Any]],
    properties: Mapping[str, Any],
) -> None:
    try:
        geom_type = geom_obj.geom_type
    except Exception:
        return

    if geom_type == "Polygon":
        exterior = list(getattr(geom_obj.exterior, "coords", []))
        if len(exterior) >= 4:
            out_features.append(
                {
                    "type": "Feature",
                    "properties": dict(properties),
                    "geometry": {"type": "Polygon", "coordinates": [[[x, y] for x, y in exterior]]},
                }
            )
        return

    if geom_type in {"MultiPolygon", "GeometryCollection"}:
        for part in getattr(geom_obj, "geoms", []):
            _append_polygon_features(part, out_features, properties)


def _section_rotation_angle_degrees(poly) -> float:
    try:
        mrr = poly.minimum_rotated_rectangle
        ring = list(mrr.exterior.coords)
    except Exception:
        return 0.0

    if len(ring) < 5:
        return 0.0

    points = ring[:-1]
    if len(points) < 4:
        return 0.0

    best_len = 0.0
    best_vec = (1.0, 0.0)
    best_east_score = -1.0
    for i in range(4):
        x1, y1 = points[i]
        x2, y2 = points[(i + 1) % 4]
        dx = x2 - x1
        dy = y2 - y1
        seg_len = math.hypot(dx, dy)
        east_score = abs(dx) - abs(dy)
        if east_score > best_east_score and seg_len > 1e-12:
            best_east_score = east_score
            best_len = seg_len
            best_vec = (dx, dy)
        elif best_east_score < 0 and seg_len > best_len:
            best_len = seg_len
            best_vec = (dx, dy)

    if best_len <= 1e-12:
        return 0.0

    vx, vy = best_vec
    # Normalize to an east-pointing axis so west/south ownership stays stable.
    if vx < 0:
        vx = -vx
        vy = -vy

    angle = math.degrees(math.atan2(vy, vx))
    if angle > 90.0:
        angle -= 180.0
    elif angle < -90.0:
        angle += 180.0
    return angle


def _line_angle_degrees(line) -> float:
    try:
        coords = list(line.coords)
    except Exception:
        return 0.0
    if len(coords) < 2:
        return 0.0
    x1, y1 = coords[0]
    x2, y2 = coords[-1]
    return math.degrees(math.atan2(y2 - y1, x2 - x1))


def _is_nearly_vertical(angle_deg: float, tolerance_deg: float = 8.0) -> bool:
    a = abs(((angle_deg + 180.0) % 360.0) - 180.0)
    return abs(a - 90.0) <= tolerance_deg


def _is_nearly_horizontal(angle_deg: float, tolerance_deg: float = 8.0) -> bool:
    a = abs(((angle_deg + 180.0) % 360.0) - 180.0)
    return min(a, abs(180.0 - a)) <= tolerance_deg


def _build_section_reference_feature_collections(
    sections_fc: Optional[Mapping[str, Any]],
) -> Tuple[Optional[Dict[str, Any]], Optional[Dict[str, Any]], Optional[Dict[str, Any]]]:
    if sections_fc is None or not isinstance(sections_fc, Mapping):
        return None, None, None

    try:
        from shapely.geometry import LineString, Polygon
    except Exception:
        return None, None, None

    features = sections_fc.get("features")
    if not isinstance(features, list):
        return None, None, None

    hatch_features: list[Dict[str, Any]] = []
    west_features: list[Dict[str, Any]] = []
    south_features: list[Dict[str, Any]] = []

    for feature in features:
        if not isinstance(feature, Mapping):
            continue
        geom = feature.get("geometry")
        if not isinstance(geom, Mapping):
            continue
        coords = geom.get("coordinates")
        if not isinstance(coords, list):
            continue

        section_keys = sorted(_extract_section_keys(feature))
        section_key = section_keys[0] if section_keys else ""

        polygon_rings = []
        if geom.get("type") == "Polygon" and coords:
            polygon_rings.append(coords[0])
        elif geom.get("type") == "MultiPolygon":
            for poly in coords:
                if isinstance(poly, list) and poly:
                    polygon_rings.append(poly[0])

        for ring in polygon_rings:
            if not isinstance(ring, list) or len(ring) < 4:
                continue

            try:
                poly = Polygon(ring)
            except Exception:
                continue

            if poly.is_empty:
                continue
            if not poly.is_valid:
                poly = poly.buffer(0)
                if poly.is_empty:
                    continue

            minx, miny, maxx, maxy = poly.bounds
            span_x = maxx - minx
            span_y = maxy - miny
            span = max(span_x, span_y, 1e-9)

            # Diagonal hatch clipped to each section polygon for fast visual section identity.
            spacing = max(span / 22.0, 0.00012)
            diag = span_y if span_y > 1e-12 else span
            x0 = minx - diag
            x1 = maxx + diag
            c = x0
            while c <= x1:
                hatch_line = LineString([(c, miny), (c + diag, maxy)])
                clipped = poly.intersection(hatch_line)
                _append_linestring_features(
                    clipped,
                    hatch_features,
                    {"section_key": section_key, "ref": "section_hatch"},
                )
                c += spacing

            segs = []
            closed_ring = list(ring)
            if closed_ring[0] != closed_ring[-1]:
                closed_ring.append(closed_ring[0])
            for a, b in zip(closed_ring, closed_ring[1:]):
                if not isinstance(a, (list, tuple)) or not isinstance(b, (list, tuple)):
                    continue
                if len(a) < 2 or len(b) < 2:
                    continue
                x1s, y1s = float(a[0]), float(a[1])
                x2s, y2s = float(b[0]), float(b[1])
                if math.hypot(x2s - x1s, y2s - y1s) <= 1e-12:
                    continue
                segs.append(((x1s, y1s), (x2s, y2s), 0.5 * (x1s + x2s), 0.5 * (y1s + y2s)))

            if not segs:
                continue

            min_mid_x = min(s[2] for s in segs)
            min_mid_y = min(s[3] for s in segs)
            tol_x = max(span_x * 0.07, 1e-9)
            tol_y = max(span_y * 0.07, 1e-9)

            for (sx, sy), (ex, ey), mx, my in segs:
                if mx <= min_mid_x + tol_x:
                    west_features.append(
                        {
                            "type": "Feature",
                            "properties": {"section_key": section_key, "ref": "west_boundary"},
                            "geometry": {"type": "LineString", "coordinates": [[sx, sy], [ex, ey]]},
                        }
                    )
                if my <= min_mid_y + tol_y:
                    south_features.append(
                        {
                            "type": "Feature",
                            "properties": {"section_key": section_key, "ref": "south_boundary"},
                            "geometry": {"type": "LineString", "coordinates": [[sx, sy], [ex, ey]]},
                        }
                    )

    hatch_fc = {"type": "FeatureCollection", "features": hatch_features} if hatch_features else None
    west_fc = {"type": "FeatureCollection", "features": west_features} if west_features else None
    south_fc = {"type": "FeatureCollection", "features": south_features} if south_features else None
    return hatch_fc, west_fc, south_fc


def _build_quarter_reference_feature_collections(
    sections_fc: Optional[Mapping[str, Any]],
    cad_lines_fc: Optional[Mapping[str, Any]] = None,
    ownership_width_m: float = QUARTER_OWNERSHIP_WIDTH_M,
) -> Dict[str, Tuple[Optional[Dict[str, Any]], Optional[Dict[str, Any]]]]:
    output: Dict[str, Tuple[Optional[Dict[str, Any]], Optional[Dict[str, Any]]]] = {
        "NW": (None, None),
        "NE": (None, None),
        "SW": (None, None),
        "SE": (None, None),
    }
    if sections_fc is None or not isinstance(sections_fc, Mapping):
        return output

    try:
        from shapely.geometry import LineString, Polygon, box
        from shapely import affinity, ops
        from pyproj import Transformer
    except Exception:
        return output

    features = sections_fc.get("features")
    if not isinstance(features, list):
        return output

    fill: Dict[str, list[Dict[str, Any]]] = {"NW": [], "NE": [], "SW": [], "SE": []}
    hatch: Dict[str, list[Dict[str, Any]]] = {"NW": [], "NE": [], "SW": [], "SE": []}
    to_utm_cache: Dict[int, Any] = {}
    to_ll_cache: Dict[int, Any] = {}
    cad_lines_by_zone: Dict[int, list[Tuple[Any, str]]] = {}

    cad_features = cad_lines_fc.get("features") if isinstance(cad_lines_fc, Mapping) else None
    if isinstance(cad_features, list):
        for cad_feature in cad_features:
            if not isinstance(cad_feature, Mapping):
                continue
            cad_props = cad_feature.get("properties")
            cad_zone = _parse_zone(cad_props.get("zone")) if isinstance(cad_props, Mapping) else None
            if cad_zone is None:
                continue
            cad_layer = (
                str(cad_props.get("layer", "")).strip().upper() if isinstance(cad_props, Mapping) else ""
            )
            cad_geom = cad_feature.get("geometry")
            if not isinstance(cad_geom, Mapping):
                continue
            cad_coords = cad_geom.get("coordinates")
            if not isinstance(cad_coords, list) or len(cad_coords) < 2:
                continue
            if cad_geom.get("type") != "LineString":
                continue

            to_utm = to_utm_cache.get(cad_zone)
            to_ll = to_ll_cache.get(cad_zone)
            if to_utm is None or to_ll is None:
                to_utm = Transformer.from_crs("EPSG:4326", f"EPSG:326{cad_zone}", always_xy=True)
                to_ll = Transformer.from_crs(f"EPSG:326{cad_zone}", "EPSG:4326", always_xy=True)
                to_utm_cache[cad_zone] = to_utm
                to_ll_cache[cad_zone] = to_ll

            try:
                first_x, first_y = float(cad_coords[0][0]), float(cad_coords[0][1])
            except Exception:
                continue

            cad_coords_utm = []
            if -180.0 <= first_x <= 180.0 and -90.0 <= first_y <= 90.0:
                for pt in cad_coords:
                    if not isinstance(pt, (list, tuple)) or len(pt) < 2:
                        continue
                    x, y = to_utm.transform(float(pt[0]), float(pt[1]))
                    cad_coords_utm.append((x, y))
            else:
                for pt in cad_coords:
                    if not isinstance(pt, (list, tuple)) or len(pt) < 2:
                        continue
                    cad_coords_utm.append((float(pt[0]), float(pt[1])))

            if len(cad_coords_utm) < 2:
                continue
            try:
                cad_line = LineString(cad_coords_utm)
            except Exception:
                continue
            if cad_line.is_empty or cad_line.length <= 1e-6:
                continue
            cad_lines_by_zone.setdefault(cad_zone, []).append((cad_line, cad_layer))

    for feature in features:
        if not isinstance(feature, Mapping):
            continue
        props = feature.get("properties")
        zone = _parse_zone(props.get("zone")) if isinstance(props, Mapping) else None
        to_utm = None
        to_ll = None
        if zone is not None:
            to_utm = to_utm_cache.get(zone)
            to_ll = to_ll_cache.get(zone)
            if to_utm is None or to_ll is None:
                to_utm = Transformer.from_crs("EPSG:4326", f"EPSG:326{zone}", always_xy=True)
                to_ll = Transformer.from_crs(f"EPSG:326{zone}", "EPSG:4326", always_xy=True)
                to_utm_cache[zone] = to_utm
                to_ll_cache[zone] = to_ll
        geom = feature.get("geometry")
        if not isinstance(geom, Mapping):
            continue
        coords = geom.get("coordinates")
        if not isinstance(coords, list):
            continue

        section_keys = sorted(_extract_section_keys(feature))
        section_key = section_keys[0] if section_keys else ""

        polygon_rings = []
        if geom.get("type") == "Polygon" and coords:
            polygon_rings.append(coords[0])
        elif geom.get("type") == "MultiPolygon":
            for poly in coords:
                if isinstance(poly, list) and poly:
                    polygon_rings.append(poly[0])

        for ring in polygon_rings:
            if not isinstance(ring, list) or len(ring) < 4:
                continue

            if to_utm is not None:
                ring_proc = []
                for pt in ring:
                    if not isinstance(pt, (list, tuple)) or len(pt) < 2:
                        continue
                    x, y = to_utm.transform(float(pt[0]), float(pt[1]))
                    ring_proc.append([x, y])
            else:
                ring_proc = ring

            try:
                poly = Polygon(ring_proc)
            except Exception:
                continue

            if poly.is_empty:
                continue
            if not poly.is_valid:
                poly = poly.buffer(0)
                if poly.is_empty:
                    continue

            angle_deg = _section_rotation_angle_degrees(poly)
            poly_rot = affinity.rotate(poly, -angle_deg, origin=(0, 0))
            if poly_rot.is_empty:
                continue
            if not poly_rot.is_valid:
                poly_rot = poly_rot.buffer(0)
                if poly_rot.is_empty:
                    continue

            angle_rad = math.radians(angle_deg)
            east_x = math.cos(angle_rad)
            east_y = math.sin(angle_rad)
            if east_x < 0:
                east_x = -east_x
                east_y = -east_y
            north_x = -east_y
            north_y = east_x
            if north_y < 0:
                north_x = -north_x
                north_y = -north_y
            center = poly.centroid

            minx, miny, maxx, maxy = poly_rot.bounds
            span_x = max(maxx - minx, 1e-12)
            span_y = max(maxy - miny, 1e-12)
            midx = 0.5 * (minx + maxx)
            midy = 0.5 * (miny + maxy)

            if to_utm is not None:
                width_m = max(0.0, min(40.0, float(ownership_width_m)))
                west_pad = width_m
                south_pad = width_m

                zone_lines = cad_lines_by_zone.get(zone, [])
                west_strip = box(minx - west_pad, miny, minx, maxy)
                south_strip = box(minx, miny - south_pad, maxx, miny)
                if zone_lines:
                    ub = poly.bounds
                    search_window = box(ub[0] - 80.0, ub[1] - 80.0, ub[2] + 80.0, ub[3] + 80.0)
                    west_candidates: list[Tuple[float, float, float, float]] = []
                    south_candidates: list[Tuple[float, float, float, float]] = []
                    nearby_boundary_lines = 0
                    for cad_line, cad_layer in zone_lines:
                        if cad_layer not in {"L-SEC", "L-USEC"}:
                            continue
                        if not cad_line.intersects(search_window):
                            continue
                        nearby_boundary_lines += 1
                        cad_rot = affinity.rotate(cad_line, -angle_deg, origin=(0, 0))
                        cb = cad_rot.bounds
                        overlap_y = max(0.0, min(maxy, cb[3]) - max(miny, cb[1]))
                        overlap_x = max(0.0, min(maxx, cb[2]) - max(minx, cb[0]))
                        west_overlap_min = max(4.0, span_y * 0.02)
                        south_overlap_min = max(4.0, span_x * 0.02)
                        if cb[2] <= minx + 4.0 and cb[0] >= minx - 55.0 and overlap_y >= west_overlap_min:
                            if _is_nearly_vertical(_line_angle_degrees(cad_rot)):
                                dist_w = minx - cb[2]
                                if 0.5 <= dist_w <= 40.0:
                                    west_candidates.append((dist_w, max(miny, cb[1]), min(maxy, cb[3]), overlap_y))
                        if cb[3] <= miny + 4.0 and cb[1] >= miny - 55.0 and overlap_x >= south_overlap_min:
                            if _is_nearly_horizontal(_line_angle_degrees(cad_rot)):
                                dist_s = miny - cb[3]
                                if 0.5 <= dist_s <= 40.0:
                                    south_candidates.append((dist_s, max(minx, cb[0]), min(maxx, cb[2]), overlap_x))

                    def _pick_target_distance(values: list[float], default: float) -> float:
                        if not values:
                            return default
                        strong = [v for v in values if v >= 15.0]
                        pool = strong if strong else values
                        return min(
                            pool,
                            key=lambda v: (min(abs(v - 20.11), abs(v - 30.17)), abs(v - default)),
                        )

                    if west_candidates:
                        west_target = _pick_target_distance([v[0] for v in west_candidates], width_m)
                        west_pad = max(width_m, west_target)
                        west_keep_overlap = max(4.0, span_y * 0.02)
                        west_parts = [
                            box(minx - west_pad, y0, minx, y1)
                            for dist, y0, y1, overlap in west_candidates
                            if abs(dist - west_target) <= 2.0 and overlap >= west_keep_overlap and y1 > y0
                        ]
                        if west_parts:
                            west_strip = ops.unary_union(west_parts).intersection(box(minx - west_pad, miny, minx, maxy))

                    if south_candidates:
                        south_target = _pick_target_distance([v[0] for v in south_candidates], width_m)
                        south_pad = max(width_m, south_target)
                        south_keep_overlap = max(4.0, span_x * 0.02)
                        south_parts = [
                            box(x0, miny - south_pad, x1, miny)
                            for dist, x0, x1, overlap in south_candidates
                            if abs(dist - south_target) <= 2.0 and overlap >= south_keep_overlap and x1 > x0
                        ]
                        if south_parts:
                            south_strip = ops.unary_union(south_parts).intersection(box(minx, miny - south_pad, maxx, miny))
                    elif nearby_boundary_lines > 0:
                        # Avoid over-extending S.1/2 quarter ownership where a blind line exists
                        # but does not span the full section width.
                        south_strip = Polygon()
            else:
                ratio = max(0.0, min(0.15, QUARTER_OWNERSHIP_RATIO_FALLBACK))
                west_pad = span_x * ratio
                south_pad = span_y * ratio
                west_strip = box(minx - west_pad, miny, minx, maxy)
                south_strip = box(minx, miny - south_pad, maxx, miny)

            sw_corner_strip = west_strip.intersection(south_strip)
            owned_poly_rot = poly_rot.union(west_strip).union(south_strip).union(sw_corner_strip)

            quarters = {
                "SW": box(minx - west_pad, miny - south_pad, midx, midy),
                "SE": box(midx, miny - south_pad, maxx, midy),
                "NW": box(minx - west_pad, midy, midx, maxy),
                "NE": box(midx, midy, maxx, maxy),
            }

            for quarter_id, quarter_box in quarters.items():
                clipped_rot = owned_poly_rot.intersection(quarter_box)
                if clipped_rot.is_empty:
                    continue

                clipped = affinity.rotate(clipped_rot, angle_deg, origin=(0, 0))
                if to_ll is not None:
                    clipped = ops.transform(lambda x, y, z=None: to_ll.transform(x, y), clipped)
                clipped_centroid = affinity.rotate(clipped_rot.centroid, angle_deg, origin=(0, 0))
                dxc = clipped_centroid.x - center.x
                dyc = clipped_centroid.y - center.y
                east_score = dxc * east_x + dyc * east_y
                north_score = dxc * north_x + dyc * north_y
                mapped_quarter_id = ("N" if north_score >= 0 else "S") + ("W" if east_score < 0 else "E")
                props = {"section_key": section_key, "quarter": mapped_quarter_id}
                _append_polygon_features(clipped, fill[mapped_quarter_id], props)

                qminx, qminy, qmaxx, qmaxy = clipped_rot.bounds
                qspan = max(qmaxx - qminx, qmaxy - qminy, 1e-9)
                spacing = max(qspan / 16.0, 0.00006)
                diag = (qmaxy - qminy) if (qmaxy - qminy) > 1e-12 else qspan
                x0 = qminx - diag
                x1 = qmaxx + diag
                cursor = x0
                while cursor <= x1:
                    hatch_line = LineString([(cursor, qminy), (cursor + diag, qmaxy)])
                    hatched_rot = clipped_rot.intersection(hatch_line)
                    hatched = affinity.rotate(hatched_rot, angle_deg, origin=(0, 0))
                    if to_ll is not None:
                        hatched = ops.transform(lambda x, y, z=None: to_ll.transform(x, y), hatched)
                    _append_linestring_features(hatched, hatch[mapped_quarter_id], props)
                    cursor += spacing

    for quarter_id in ("NW", "NE", "SW", "SE"):
        fill_fc = {"type": "FeatureCollection", "features": fill[quarter_id]} if fill[quarter_id] else None
        hatch_fc = {"type": "FeatureCollection", "features": hatch[quarter_id]} if hatch[quarter_id] else None
        output[quarter_id] = (fill_fc, hatch_fc)

    return output


def _build_nw_rule_preview_feature_collections(
    sections_fc: Optional[Mapping[str, Any]],
    cad_lines_fc: Optional[Mapping[str, Any]],
    include_apparent_extensions: bool = False,
) -> Dict[str, Optional[Dict[str, Any]]]:
    output: Dict[str, Optional[Dict[str, Any]]] = {
        "nw_rule_selected": None,
        "nw_rule_targets": None,
        "nw_rule_joins": None,
        "nw_rule_unresolved": None,
    }
    if not isinstance(sections_fc, Mapping) or not isinstance(cad_lines_fc, Mapping):
        return output

    try:
        from shapely.geometry import LineString, Polygon, box
        from shapely import affinity
    except Exception:
        return output

    section_features = sections_fc.get("features")
    cad_features = cad_lines_fc.get("features")
    if not isinstance(section_features, list) or not isinstance(cad_features, list):
        return output

    def _iter_line_parts(geom_obj):
        try:
            geom_type = geom_obj.geom_type
        except Exception:
            return
        if geom_type == "LineString":
            if geom_obj.length > 1e-9:
                yield geom_obj
            return
        if geom_type in {"MultiLineString", "GeometryCollection"}:
            for part in getattr(geom_obj, "geoms", []):
                yield from _iter_line_parts(part)

    cad_lines: list[Tuple[Any, str, str, str]] = []
    for cad_feature in cad_features:
        if not isinstance(cad_feature, Mapping):
            continue
        props = cad_feature.get("properties")
        layer = str(props.get("layer", "")).strip().upper() if isinstance(props, Mapping) else ""
        handle = str(props.get("handle", "")).strip().upper() if isinstance(props, Mapping) else ""
        line_id = str(props.get("line_id", "")).strip() if isinstance(props, Mapping) else ""
        if layer not in {"L-SEC", "L-USEC"}:
            continue
        geom = cad_feature.get("geometry")
        if not isinstance(geom, Mapping):
            continue
        coords = geom.get("coordinates")
        if not isinstance(coords, list) or len(coords) < 2:
            continue
        if geom.get("type") != "LineString":
            continue
        try:
            line = LineString([(float(pt[0]), float(pt[1])) for pt in coords if isinstance(pt, (list, tuple)) and len(pt) >= 2])
        except Exception:
            continue
        if line.is_empty or line.length <= 1e-9:
            continue
        cad_lines.append((line, layer, handle, line_id))

    if not cad_lines:
        return output

    selected_features: list[Dict[str, Any]] = []
    target_features: list[Dict[str, Any]] = []
    join_features: list[Dict[str, Any]] = []
    unresolved_features: list[Dict[str, Any]] = []

    for section_feature in section_features:
        if not isinstance(section_feature, Mapping):
            continue
        section_props = section_feature.get("properties")
        section_keys = sorted(_extract_section_keys(section_feature))
        section_key = section_keys[0] if section_keys else ""

        geom = section_feature.get("geometry")
        if not isinstance(geom, Mapping):
            continue
        coords = geom.get("coordinates")
        if not isinstance(coords, list):
            continue

        polygon_rings = []
        if geom.get("type") == "Polygon" and coords:
            polygon_rings.append(coords[0])
        elif geom.get("type") == "MultiPolygon":
            for poly in coords:
                if isinstance(poly, list) and poly:
                    polygon_rings.append(poly[0])

        for ring in polygon_rings:
            if not isinstance(ring, list) or len(ring) < 4:
                continue
            try:
                poly = Polygon(ring)
            except Exception:
                continue
            if poly.is_empty:
                continue
            if not poly.is_valid:
                poly = poly.buffer(0)
                if poly.is_empty:
                    continue

            minx0, miny0, maxx0, maxy0 = poly.bounds
            is_lonlat = (
                -180.0 <= minx0 <= 180.0
                and -180.0 <= maxx0 <= 180.0
                and -90.0 <= miny0 <= 90.0
                and -90.0 <= maxy0 <= 90.0
                and max(maxx0 - minx0, maxy0 - miny0) < 5.0
            )
            lat = poly.centroid.y
            meters_per_x = 111320.0 * max(0.1, math.cos(math.radians(lat))) if is_lonlat else 1.0
            meters_per_y = 111320.0 if is_lonlat else 1.0
            ux = 1.0 / max(meters_per_x, 1.0)
            uy = 1.0 / max(meters_per_y, 1.0)

            west_band = NW_RULE_WEST_BAND_M * ux
            north_band = NW_RULE_NORTH_BAND_M * uy
            join_min = NW_RULE_JOIN_MIN_M * uy
            join_max = NW_RULE_JOIN_MAX_M * uy
            span_tol_x = 0.35 * ux
            hit_tol = 0.35 * max(ux, uy)

            angle_deg = _section_rotation_angle_degrees(poly)
            poly_rot = affinity.rotate(poly, -angle_deg, origin=(0, 0))
            if poly_rot.is_empty:
                continue
            if not poly_rot.is_valid:
                poly_rot = poly_rot.buffer(0)
                if poly_rot.is_empty:
                    continue

            minx, miny, maxx, maxy = poly_rot.bounds
            midx = 0.5 * (minx + maxx)
            midy = 0.5 * (miny + maxy)
            nw_west_window = box(
                minx - west_band - (10.0 * ux),
                miny - (5.0 * uy),
                minx + (10.0 * ux),
                maxy + (10.0 * uy),
            )
            west_band_max_x = minx + west_band
            north_band_min_y = maxy - north_band
            search_window = box(minx - (120.0 * ux), miny - (120.0 * uy), maxx + (120.0 * ux), maxy + (120.0 * uy))

            vertical_candidates: list[Tuple[float, float, Any, str, float, str, str]] = []
            for cad_line, cad_layer, cad_handle, cad_line_id in cad_lines:
                if cad_layer not in {"L-SEC", "L-USEC"}:
                    continue
                cad_rot = affinity.rotate(cad_line, -angle_deg, origin=(0, 0))
                if not cad_rot.intersects(search_window):
                    continue
                clipped = cad_rot.intersection(nw_west_window)
                for part in _iter_line_parts(clipped):
                    if not _is_nearly_vertical(_line_angle_degrees(part)):
                        continue
                    cb = part.bounds
                    mid_x = 0.5 * (cb[0] + cb[2])
                    if mid_x < (minx - west_band - span_tol_x):
                        continue
                    if mid_x > (west_band_max_x + span_tol_x):
                        continue
                    north_y = max(cb[1], cb[3])
                    south_y = min(cb[1], cb[3])
                    length_m = part.length / max(uy, 1e-12)
                    vertical_candidates.append((mid_x, north_y, part, cad_layer, length_m, cad_handle, cad_line_id))

            if not vertical_candidates:
                continue

            sec_vertical_candidates = [row for row in vertical_candidates if row[3] == "L-SEC"]
            selected_pool = sec_vertical_candidates if sec_vertical_candidates else vertical_candidates
            selected_pool.sort(key=lambda row: row[0])  # west -> east

            cluster_tol = NW_RULE_SELECTED_X_CLUSTER_M * ux
            clusters: list[list[Tuple[float, float, Any, str, float]]] = []
            for row in selected_pool:
                if not clusters:
                    clusters.append([row])
                    continue
                last_cluster = clusters[-1]
                last_x = sum(item[0] for item in last_cluster) / len(last_cluster)
                if abs(row[0] - last_x) <= cluster_tol:
                    last_cluster.append(row)
                else:
                    clusters.append([row])

            if len(clusters) < 2:
                # Keep parity with plugin safety rule: never fall back to westernmost
                # when the "second-most-west" cluster does not exist.
                continue
            cluster_index = 1
            chosen_cluster = clusters[cluster_index]
            non_stub_cluster = [row for row in chosen_cluster if row[4] >= NW_RULE_MIN_VERTICAL_SEGMENT_M]
            ranked_pool = non_stub_cluster or chosen_cluster
            ranked_pool.sort(key=lambda row: (row[1], row[4], row[0]), reverse=True)
            ranked_pool = [row for row in ranked_pool if row[1] >= (midy - (2.0 * uy))]
            if not ranked_pool:
                continue
            selected_rot = ranked_pool[0][2]
            selected_layer = ranked_pool[0][3]
            selected_length_m = ranked_pool[0][4]
            selected_handle = ranked_pool[0][5]
            selected_line_id = ranked_pool[0][6]
            selected_coords = list(selected_rot.coords)
            if len(selected_coords) < 2:
                continue
            north_endpoint = max(selected_coords, key=lambda p: p[1])
            south_endpoint = min(selected_coords, key=lambda p: p[1])
            marker_min_y = max(float(south_endpoint[1]), float(north_endpoint[1]) - (NW_RULE_SELECTED_MARKER_M * uy))
            selected_marker_rot = LineString(
                [
                    (float(north_endpoint[0]), marker_min_y),
                    (float(north_endpoint[0]), float(north_endpoint[1])),
                ]
            )
            selected_world = affinity.rotate(selected_marker_rot, angle_deg, origin=(0, 0))
            selected_props = {
                "section_key": section_key,
                "rule": "nw_second_west_20.11",
                "status": "selected",
                "layer": selected_layer,
                "fallback": "0" if selected_layer == "L-SEC" else "1",
                "x_rank": str(cluster_index + 1),
                "length_m": round(selected_length_m, 3),
                "source_handle": selected_handle,
                "source_line_id": selected_line_id,
                "line_id": f"NWSEL:{section_key}:{selected_line_id or selected_handle or 'NA'}",
            }
            _append_linestring_features(selected_world, selected_features, selected_props)

            vertical_x = float(north_endpoint[0])
            north_y = float(north_endpoint[1])

            endpoint_tol = NW_RULE_ENDPOINT_TOUCH_TOL_M * uy
            best_target = None
            best_join_dist = float("inf")
            direct_endpoint_target = None
            direct_endpoint_dist = float("inf")
            best_apparent_target = None
            best_apparent_score = float("inf")
            crossing_count = 0
            nearest_crossing_join_abs = float("inf")
            nearest_crossing_join = 0.0
            north_window = box(minx - (5.0 * ux), north_band_min_y, maxx + (5.0 * ux), maxy + (45.0 * uy))

            for cad_line, cad_layer, cad_handle, cad_line_id in cad_lines:
                if cad_layer not in {"L-SEC", "L-USEC"}:
                    continue
                cad_rot = affinity.rotate(cad_line, -angle_deg, origin=(0, 0))
                if not cad_rot.intersects(search_window):
                    continue
                clipped = cad_rot.intersection(north_window)
                for part in _iter_line_parts(clipped):
                    if not _is_nearly_horizontal(_line_angle_degrees(part)):
                        continue
                    pts = list(part.coords)
                    if len(pts) < 2:
                        continue
                    x1, y1 = pts[0]
                    x2, y2 = pts[-1]
                    min_x = min(x1, x2) - span_tol_x
                    max_x = max(x1, x2) + span_tol_x
                    dx = x2 - x1
                    if abs(dx) <= 1e-12:
                        continue
                    t = (vertical_x - x1) / dx
                    y_at_x = y1 + (t * (y2 - y1))
                    join_dist = y_at_x - north_y
                    crossing_count += 1
                    join_abs = abs(join_dist)
                    if join_abs < nearest_crossing_join_abs:
                        nearest_crossing_join_abs = join_abs
                        nearest_crossing_join = join_dist
                    endpoint_touch = abs(join_dist) <= endpoint_tol
                    regular_join = join_min < join_dist <= join_max
                    if not endpoint_touch and not regular_join:
                        continue
                    join_point = (vertical_x, y_at_x)
                    spans_vertical = min_x <= vertical_x <= max_x
                    if spans_vertical:
                        if not LineString([(vertical_x, north_y), join_point]).intersects(part.buffer(hit_tol)):
                            continue
                        if endpoint_touch and abs(join_dist) < direct_endpoint_dist:
                            direct_endpoint_dist = join_dist
                            direct_endpoint_target = (part, join_point, cad_layer, join_dist, cad_handle, cad_line_id)
                        if regular_join and join_dist < best_join_dist:
                            best_join_dist = join_dist
                            best_target = (part, join_point, cad_layer, join_dist, cad_handle, cad_line_id)
                        continue

                    x_gap = (min_x - vertical_x) if vertical_x < min_x else (vertical_x - max_x)
                    if x_gap > (NW_RULE_APPARENT_SPAN_GAP_MAX_M * ux):
                        continue
                    if abs(join_dist) > (NW_RULE_APPARENT_JOIN_MAX_M * uy):
                        continue

                    endpoint_a = (x1, y1)
                    endpoint_b = (x2, y2)
                    dist_a = math.hypot(endpoint_a[0] - join_point[0], endpoint_a[1] - join_point[1])
                    dist_b = math.hypot(endpoint_b[0] - join_point[0], endpoint_b[1] - join_point[1])
                    target_endpoint = endpoint_a if dist_a <= dist_b else endpoint_b
                    endpoint_move = min(dist_a, dist_b)
                    apparent_score = endpoint_move + (2.0 * abs(join_dist))
                    if apparent_score < best_apparent_score:
                        best_apparent_score = apparent_score
                        best_apparent_target = (
                            part,
                            join_point,
                            cad_layer,
                            join_dist,
                            cad_handle,
                            cad_line_id,
                            target_endpoint,
                        )

            chosen_target = direct_endpoint_target if direct_endpoint_target is not None else best_target
            apparent_target = None
            if include_apparent_extensions and chosen_target is None and best_apparent_target is not None:
                apparent_target = best_apparent_target
                target_rot, join_point, target_layer, join_dist, target_handle, target_line_id, target_endpoint = apparent_target
            elif chosen_target is not None:
                target_rot, join_point, target_layer, join_dist, target_handle, target_line_id = chosen_target
                target_endpoint = None

            if chosen_target is not None or apparent_target is not None:
                target_world = affinity.rotate(target_rot, angle_deg, origin=(0, 0))
                endpoint_touch = abs(join_dist) <= endpoint_tol
                target_props = {
                    "section_key": section_key,
                    "rule": "nw_second_west_20.11",
                    "status": "north_target",
                    "layer": target_layer,
                    "endpoint_touch": "1" if endpoint_touch else "0",
                    "apparent_extension": "1" if apparent_target is not None else "0",
                    "source_handle": selected_handle,
                    "source_line_id": selected_line_id,
                    "target_handle": target_handle,
                    "target_line_id": target_line_id,
                    "line_id": f"NWTGT:{section_key}:{selected_line_id or selected_handle or 'NA'}->{target_line_id or target_handle or 'NA'}",
                }
                _append_linestring_features(target_world, target_features, target_props)

                if apparent_target is not None and target_endpoint is not None:
                    join_rot = LineString([target_endpoint, join_point])
                    join_world = affinity.rotate(join_rot, angle_deg, origin=(0, 0))
                    join_props = {
                        "section_key": section_key,
                        "rule": "nw_second_west_20.11",
                        "status": "join",
                        "endpoint_touch": "0",
                        "apparent_extension": "1",
                        "source_handle": selected_handle,
                        "source_line_id": selected_line_id,
                        "target_handle": target_handle,
                        "target_line_id": target_line_id,
                        "line_id": f"NWJOIN:{section_key}:{selected_line_id or selected_handle or 'NA'}->{target_line_id or target_handle or 'NA'}",
                    }
                    _append_linestring_features(join_world, join_features, join_props)
                elif not endpoint_touch:
                    join_rot = LineString([(vertical_x, north_y), join_point])
                    join_world = affinity.rotate(join_rot, angle_deg, origin=(0, 0))
                    join_props = {
                        "section_key": section_key,
                        "rule": "nw_second_west_20.11",
                        "status": "join",
                        "endpoint_touch": "0",
                        "source_handle": selected_handle,
                        "source_line_id": selected_line_id,
                        "target_handle": target_handle,
                        "target_line_id": target_line_id,
                        "line_id": f"NWJOIN:{section_key}:{selected_line_id or selected_handle or 'NA'}->{target_line_id or target_handle or 'NA'}",
                    }
                    _append_linestring_features(join_world, join_features, join_props)
            else:
                # If no north corridor is present (blind-line-style case), do not flag unresolved.
                if crossing_count == 0 or nearest_crossing_join <= join_min:
                    continue
                # Keep unresolved markers visible where section-specific tuning is needed.
                marker_rot = LineString(
                    [
                        (vertical_x - (6.0 * ux), north_y),
                        (vertical_x + (6.0 * ux), north_y),
                    ]
                )
                marker_world = affinity.rotate(marker_rot, angle_deg, origin=(0, 0))
                unresolved_props = {
                    "section_key": section_key,
                    "rule": "nw_second_west_20.11",
                    "status": "no_north_target",
                    "layer": selected_layer,
                    "fallback": "0" if selected_layer == "L-SEC" else "1",
                    "source_handle": selected_handle,
                    "source_line_id": selected_line_id,
                    "line_id": f"NWUNRES:{section_key}:{selected_line_id or selected_handle or 'NA'}",
                }
                _append_linestring_features(marker_world, unresolved_features, unresolved_props)

    if selected_features:
        output["nw_rule_selected"] = {"type": "FeatureCollection", "features": selected_features}
    if target_features:
        output["nw_rule_targets"] = {"type": "FeatureCollection", "features": target_features}
    if join_features:
        output["nw_rule_joins"] = {"type": "FeatureCollection", "features": join_features}
    if unresolved_features:
        output["nw_rule_unresolved"] = {"type": "FeatureCollection", "features": unresolved_features}

    return output


def _build_manual_join_debug_feature_collections(
    cad_lines_fc: Optional[Mapping[str, Any]],
    source_token: str,
    target_token: str,
) -> Tuple[Dict[str, Optional[Dict[str, Any]]], Dict[str, Any]]:
    output: Dict[str, Optional[Dict[str, Any]]] = {
        "manual_debug_source": None,
        "manual_debug_target": None,
        "manual_debug_source_ext": None,
        "manual_debug_target_ext": None,
        "manual_debug_intersection": None,
    }
    summary: Dict[str, Any] = {
        "enabled": False,
        "ok": False,
        "message": "",
    }

    src_text = str(source_token or "").strip()
    tgt_text = str(target_token or "").strip()
    if not src_text or not tgt_text:
        return output, summary

    summary["enabled"] = True
    if not isinstance(cad_lines_fc, Mapping):
        summary["message"] = "CAD lines are not loaded."
        return output, summary

    try:
        from shapely.geometry import LineString, Point
    except Exception:
        summary["message"] = "Shapely is not available in this environment."
        return output, summary

    def _norm(value: Any) -> str:
        return re.sub(r"\s+", "", str(value or "").strip().upper())

    features = cad_lines_fc.get("features")
    if not isinstance(features, list):
        summary["message"] = "CAD lines feature collection is empty."
        return output, summary

    indexed: list[Dict[str, Any]] = []
    for feature in features:
        if not isinstance(feature, Mapping):
            continue
        geom = feature.get("geometry")
        props = feature.get("properties")
        if not isinstance(geom, Mapping) or not isinstance(props, Mapping):
            continue
        if geom.get("type") != "LineString":
            continue
        coords = geom.get("coordinates")
        if not isinstance(coords, list) or len(coords) < 2:
            continue
        try:
            line = LineString(
                [(float(pt[0]), float(pt[1])) for pt in coords if isinstance(pt, (list, tuple)) and len(pt) >= 2]
            )
        except Exception:
            continue
        if line.is_empty or line.length <= 1e-9:
            continue

        line_id = str(props.get("line_id", "")).strip()
        handle = str(props.get("handle", "")).strip().upper()
        layer = str(props.get("layer", "")).strip().upper()
        canonical = f"CAD:{layer}:{handle}" if layer and handle else ""
        base_line_id = line_id.split("#", 1)[0] if line_id else ""
        indexed.append(
            {
                "feature": feature,
                "line": line,
                "line_id": line_id,
                "line_id_upper": _norm(line_id),
                "line_id_base_upper": _norm(base_line_id),
                "handle_upper": _norm(handle),
                "canonical_upper": _norm(canonical),
                "layer": layer,
                "handle": handle,
            }
        )

    if not indexed:
        summary["message"] = "No CAD line segments were found."
        return output, summary

    def _match_entry(token: str) -> Optional[Dict[str, Any]]:
        needle = _norm(token)
        if not needle:
            return None
        handle_part = needle
        handle_suffix: Optional[int] = None
        handle_suffix_match = re.match(r"^([A-Z0-9]+)#(\d+)$", needle)
        if handle_suffix_match:
            handle_part = handle_suffix_match.group(1)
            try:
                handle_suffix = int(handle_suffix_match.group(2))
            except Exception:
                handle_suffix = None

        best_entry = None
        best_score = -1
        for entry in indexed:
            score = -1
            if needle == entry["line_id_upper"]:
                score = 100
            elif needle == entry["line_id_base_upper"]:
                score = 97
            elif entry["line_id_upper"].endswith(":" + needle):
                score = 96
            elif needle == entry["canonical_upper"]:
                score = 92
            elif needle == entry["handle_upper"]:
                score = 90
            elif handle_part == entry["handle_upper"]:
                if handle_suffix is None:
                    score = 89
                else:
                    entry_suffix = 1
                    line_id_text = str(entry.get("line_id", "")).strip()
                    suffix_match = re.search(r"#(\d+)$", line_id_text)
                    if suffix_match:
                        try:
                            entry_suffix = int(suffix_match.group(1))
                        except Exception:
                            entry_suffix = 1
                    score = 99 if entry_suffix == handle_suffix else 60
            elif entry["line_id_upper"].startswith(needle + "#"):
                score = 85
            if score > best_score:
                best_score = score
                best_entry = entry
        return best_entry if best_score >= 0 else None

    source_entry = _match_entry(src_text)
    target_entry = _match_entry(tgt_text)
    if source_entry is None or target_entry is None:
        missing = []
        if source_entry is None:
            missing.append(f"source '{src_text}'")
        if target_entry is None:
            missing.append(f"target '{tgt_text}'")
        summary["message"] = f"Could not resolve {' and '.join(missing)} to CAD line IDs."
        return output, summary

    source_line = source_entry["line"]
    target_line = target_entry["line"]
    source_line_id = source_entry["line_id"] or source_entry["canonical_upper"] or source_entry["handle"]
    target_line_id = target_entry["line_id"] or target_entry["canonical_upper"] or target_entry["handle"]

    minx, miny, maxx, maxy = source_line.union(target_line).bounds
    is_lonlat = (
        -180.0 <= minx <= 180.0
        and -180.0 <= maxx <= 180.0
        and -90.0 <= miny <= 90.0
        and -90.0 <= maxy <= 90.0
        and max(maxx - minx, maxy - miny) < 5.0
    )
    lat = 0.5 * (source_line.centroid.y + target_line.centroid.y)
    meters_per_x = 111320.0 * max(0.1, math.cos(math.radians(lat))) if is_lonlat else 1.0
    meters_per_y = 111320.0 if is_lonlat else 1.0

    def _to_m(point: Tuple[float, float]) -> Tuple[float, float]:
        return point[0] * meters_per_x, point[1] * meters_per_y

    def _metric_line(line_obj):
        return LineString([_to_m((float(x), float(y))) for x, y in line_obj.coords])

    def _cross(a: Tuple[float, float], b: Tuple[float, float]) -> float:
        return (a[0] * b[1]) - (a[1] * b[0])

    def _intersect_infinite(
        a0: Tuple[float, float],
        a1: Tuple[float, float],
        b0: Tuple[float, float],
        b1: Tuple[float, float],
    ) -> Optional[Tuple[float, float]]:
        da = (a1[0] - a0[0], a1[1] - a0[1])
        db = (b1[0] - b0[0], b1[1] - b0[1])
        denom = _cross(da, db)
        if abs(denom) <= 1e-12:
            return None
        diff = (b0[0] - a0[0], b0[1] - a0[1])
        t = _cross(diff, db) / denom
        return (a0[0] + (da[0] * t), a0[1] + (da[1] * t))

    source_features: list[Dict[str, Any]] = []
    target_features: list[Dict[str, Any]] = []
    source_ext_features: list[Dict[str, Any]] = []
    target_ext_features: list[Dict[str, Any]] = []
    intersection_features: list[Dict[str, Any]] = []

    _append_linestring_features(
        source_line,
        source_features,
        {
            "line_id": f"MANDBG:SRC:{source_line_id}",
            "status": "manual_source",
            "source_line_id": source_line_id,
            "target_line_id": target_line_id,
            "layer": source_entry["layer"],
            "handle": source_entry["handle"],
            "name": "Manual debug source",
            "label": source_line_id,
        },
    )
    _append_linestring_features(
        target_line,
        target_features,
        {
            "line_id": f"MANDBG:TGT:{target_line_id}",
            "status": "manual_target",
            "source_line_id": source_line_id,
            "target_line_id": target_line_id,
            "layer": target_entry["layer"],
            "handle": target_entry["handle"],
            "name": "Manual debug target",
            "label": target_line_id,
        },
    )

    source_coords = list(source_line.coords)
    target_coords = list(target_line.coords)
    source_a = (float(source_coords[0][0]), float(source_coords[0][1]))
    source_b = (float(source_coords[-1][0]), float(source_coords[-1][1]))
    target_a = (float(target_coords[0][0]), float(target_coords[0][1]))
    target_b = (float(target_coords[-1][0]), float(target_coords[-1][1]))

    source_metric = _metric_line(source_line)
    target_metric = _metric_line(target_line)
    summary["source_line_id"] = source_line_id
    summary["target_line_id"] = target_line_id
    summary["source_layer"] = source_entry["layer"]
    summary["target_layer"] = target_entry["layer"]
    summary["source_target_min_dist_m"] = round(float(source_metric.distance(target_metric)), 3)

    intersection = _intersect_infinite(source_a, source_b, target_a, target_b)
    if intersection is None:
        summary["ok"] = False
        summary["message"] = "Selected lines are parallel (no apparent intersection)."
    else:
        int_x, int_y = intersection
        int_m = Point(_to_m((int_x, int_y)))

        source_a_m = _to_m(source_a)
        source_b_m = _to_m(source_b)
        target_a_m = _to_m(target_a)
        target_b_m = _to_m(target_b)

        source_end = source_a if math.hypot(source_a_m[0] - int_m.x, source_a_m[1] - int_m.y) <= math.hypot(source_b_m[0] - int_m.x, source_b_m[1] - int_m.y) else source_b
        target_end = target_a if math.hypot(target_a_m[0] - int_m.x, target_a_m[1] - int_m.y) <= math.hypot(target_b_m[0] - int_m.x, target_b_m[1] - int_m.y) else target_b

        source_end_m = _to_m(source_end)
        target_end_m = _to_m(target_end)
        source_ext_m = math.hypot(source_end_m[0] - int_m.x, source_end_m[1] - int_m.y)
        target_ext_m = math.hypot(target_end_m[0] - int_m.x, target_end_m[1] - int_m.y)
        source_on_segment = float(source_metric.distance(int_m)) <= 0.35
        target_on_segment = float(target_metric.distance(int_m)) <= 0.35

        summary["ok"] = True
        summary["message"] = "Resolved source/target and computed apparent intersection."
        summary["intersection_x"] = round(float(int_x), 8)
        summary["intersection_y"] = round(float(int_y), 8)
        summary["source_extension_m"] = round(float(source_ext_m), 3)
        summary["target_extension_m"] = round(float(target_ext_m), 3)
        summary["source_spans_intersection"] = bool(source_on_segment)
        summary["target_spans_intersection"] = bool(target_on_segment)

        _append_linestring_features(
            LineString([source_end, intersection]),
            source_ext_features,
            {
                "line_id": f"MANDBG:SRCEXT:{source_line_id}->{target_line_id}",
                "status": "manual_source_extension",
                "source_line_id": source_line_id,
                "target_line_id": target_line_id,
                "name": "Source endpoint to apparent intersection",
                "label": f"{source_ext_m:.2f} m",
                "note": "Distance from nearest source endpoint to infinite-line intersection.",
            },
        )
        _append_linestring_features(
            LineString([target_end, intersection]),
            target_ext_features,
            {
                "line_id": f"MANDBG:TGTEXT:{source_line_id}->{target_line_id}",
                "status": "manual_target_extension",
                "source_line_id": source_line_id,
                "target_line_id": target_line_id,
                "name": "Target endpoint to apparent intersection",
                "label": f"{target_ext_m:.2f} m",
                "note": "Distance from nearest target endpoint to infinite-line intersection.",
            },
        )

        dx = 5.0 / max(meters_per_x, 1.0)
        dy = 5.0 / max(meters_per_y, 1.0)
        cross_a = LineString([(int_x - dx, int_y - dy), (int_x + dx, int_y + dy)])
        cross_b = LineString([(int_x - dx, int_y + dy), (int_x + dx, int_y - dy)])
        _append_linestring_features(
            cross_a,
            intersection_features,
            {
                "line_id": f"MANDBG:X:{source_line_id}->{target_line_id}",
                "status": "manual_intersection",
                "source_line_id": source_line_id,
                "target_line_id": target_line_id,
                "name": "Apparent intersection marker",
                "label": f"{int_x:.3f}, {int_y:.3f}",
            },
        )
        _append_linestring_features(
            cross_b,
            intersection_features,
            {
                "line_id": f"MANDBG:X2:{source_line_id}->{target_line_id}",
                "status": "manual_intersection",
                "source_line_id": source_line_id,
                "target_line_id": target_line_id,
                "name": "Apparent intersection marker",
                "label": f"{int_x:.3f}, {int_y:.3f}",
            },
        )

    if source_features:
        output["manual_debug_source"] = {"type": "FeatureCollection", "features": source_features}
    if target_features:
        output["manual_debug_target"] = {"type": "FeatureCollection", "features": target_features}
    if source_ext_features:
        output["manual_debug_source_ext"] = {"type": "FeatureCollection", "features": source_ext_features}
    if target_ext_features:
        output["manual_debug_target_ext"] = {"type": "FeatureCollection", "features": target_ext_features}
    if intersection_features:
        output["manual_debug_intersection"] = {"type": "FeatureCollection", "features": intersection_features}

    return output, summary


def _read_query_param(query_params: Any, key: str) -> str:
    value = query_params.get(key, "")
    if isinstance(value, list):
        return value[0] if value else ""
    if isinstance(value, tuple):
        return value[0] if value else ""
    if value is None:
        return ""
    return str(value)


def _read_argv_data_dir() -> str:
    for idx, arg in enumerate(sys.argv[1:]):
        if arg == "--data-dir" and idx + 1 < len(sys.argv) - 1:
            return str(sys.argv[idx + 2])
        if arg.startswith("--data-dir="):
            return arg.split("=", 1)[1]
    return ""


